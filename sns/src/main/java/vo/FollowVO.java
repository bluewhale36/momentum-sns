package vo;

public class FollowVO {
	
	private String id;	// 나를 팔로우하는 id
	private String followId;	// 내가 팔로우하는 id
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFollowId() {
		return followId;
	}
	public void setFollowId(String followId) {
		this.followId = followId;
	}
	
}
